from __future__ import annotations

import json
from pathlib import Path
from typing import Any

from minigpt.model_capability_route_promotion_bounded_objective_decoder_anchor_policy_review import (
    BOUNDED_OBJECTIVE_DECODER_ANCHOR_POLICY_REVIEW_JSON_FILENAME,
)
from minigpt.report_utils import as_dict, list_of_dicts, utc_now
from minigpt.report_check_common import check_entry as _check
from minigpt.report_check_common import resolve_exit_code_plan_ready as resolve_exit_code


BOUNDED_OBJECTIVE_UNASSISTED_REPAIR_PLAN_JSON_FILENAME = "model_capability_route_promotion_bounded_objective_unassisted_repair_plan.json"
BOUNDED_OBJECTIVE_UNASSISTED_REPAIR_PLAN_CSV_FILENAME = "model_capability_route_promotion_bounded_objective_unassisted_repair_plan.csv"
BOUNDED_OBJECTIVE_UNASSISTED_REPAIR_PLAN_TEXT_FILENAME = "model_capability_route_promotion_bounded_objective_unassisted_repair_plan.txt"
BOUNDED_OBJECTIVE_UNASSISTED_REPAIR_PLAN_MARKDOWN_FILENAME = "model_capability_route_promotion_bounded_objective_unassisted_repair_plan.md"
BOUNDED_OBJECTIVE_UNASSISTED_REPAIR_PLAN_HTML_FILENAME = "model_capability_route_promotion_bounded_objective_unassisted_repair_plan.html"


def locate_decoder_anchor_policy_review(path: str | Path) -> Path:
    source = Path(path)
    if source.is_dir():
        source = source / BOUNDED_OBJECTIVE_DECODER_ANCHOR_POLICY_REVIEW_JSON_FILENAME
    return source


def read_json_report(path: str | Path) -> dict[str, Any]:
    payload = json.loads(Path(path).read_text(encoding="utf-8-sig"))
    if not isinstance(payload, dict):
        raise ValueError("bounded objective unassisted repair plan input must be a JSON object")
    return dict(payload)


def build_model_capability_route_promotion_bounded_objective_unassisted_repair_plan(
    decoder_anchor_policy_review: dict[str, Any],
    *,
    policy_review_path: str | Path | None = None,
    title: str = "MiniGPT model capability route promotion bounded objective unassisted repair plan",
    generated_at: str | None = None,
) -> dict[str, Any]:
    review_summary = as_dict(decoder_anchor_policy_review.get("summary"))
    signals = as_dict(decoder_anchor_policy_review.get("signals"))
    recommendations = list_of_dicts(decoder_anchor_policy_review.get("recommendations"))
    repair_work_items = _repair_work_items()
    acceptance_gates = _acceptance_gates()
    blocked_actions = _blocked_actions()
    checks = _checks(decoder_anchor_policy_review, review_summary, signals)
    issues = [row for row in checks if row["status"] != "pass"]
    status = "pass" if not issues else "fail"
    plan = _plan(status, repair_work_items, acceptance_gates, blocked_actions)
    return {
        "schema_version": 1,
        "title": title,
        "generated_at": generated_at or utc_now(),
        "status": status,
        "decision": _decision(status),
        "failed_count": len(issues),
        "issues": issues,
        "source_policy_review": str(policy_review_path or ""),
        "review_summary": review_summary,
        "review_signals": signals,
        "source_recommendations": recommendations,
        "repair_work_items": repair_work_items,
        "acceptance_gates": acceptance_gates,
        "blocked_actions": blocked_actions,
        "check_rows": checks,
        "plan": plan,
        "summary": _summary(status, checks, plan),
        "interpretation": _interpretation(status, plan),
    }


def _repair_work_items() -> list[dict[str, Any]]:
    return [
        _work("unassisted_seed_revision", "high", "Build a seed corpus whose training examples require the model to emit fixed loss without injected decoder anchors.", "model_capability_route_promotion_bounded_objective_unassisted_repair_seed"),
        _work("first_token_targeting", "high", "Add examples and diagnostics that specifically test generation of the first token fixed from an empty answer prefix.", "first_token_uptake_report"),
        _work("loss_token_targeting", "high", "Add examples and diagnostics that test generation of the second token loss after fixed is generated by the model, not provided by an anchor.", "second_token_uptake_report"),
        _work("unassisted_training_run", "high", "Train a short repair checkpoint from the unassisted seed and keep quality claims gated behind replay.", "model_capability_route_promotion_bounded_objective_unassisted_repair_training_run"),
        _work("unchanged_holdout_replay", "high", "Replay the unchanged objective contract and the bounded route holdout before any promotion decision.", "model_capability_route_promotion_bounded_objective_unassisted_holdout_replay"),
    ]


def _work(work_id: str, priority: str, description: str, output: str) -> dict[str, Any]:
    return {"id": work_id, "priority": priority, "description": description, "expected_output": output}


def _acceptance_gates() -> list[dict[str, Any]]:
    return [
        {"id": "no_decoder_anchor_inference", "required": True, "detail": "Replay prompts must not append prefix_f, fixed, fixed l, or other decoder anchors."},
        {"id": "new_text_required_term_hit", "required": True, "detail": "At least one replay case must hit fixed/loss in continuation text without anchor assistance."},
        {"id": "objective_contract_replay", "required": True, "detail": "The unchanged bounded objective contract must be replayed before promotion review."},
        {"id": "holdout_before_promotion", "required": True, "detail": "Any unassisted recovery must be checked against unchanged holdout cases."},
    ]


def _blocked_actions() -> list[dict[str, Any]]:
    return [
        {"id": "do_not_promote_anchor_policy", "detail": "v842-v843 anchor policy artifacts are diagnostic and must not be promoted as model capability."},
        {"id": "do_not_train_more_anchor_variants", "detail": "Further anchor-only training would repeat the assisted path closed by v844."},
        {"id": "do_not_claim_quality_from_loss_only", "detail": "Loss reduction alone is insufficient without unassisted replay recovery."},
    ]


def _checks(review: dict[str, Any], summary: dict[str, Any], signals: dict[str, Any]) -> list[dict[str, Any]]:
    return [
        _check("policy_review_passed", review.get("status") == "pass", review.get("status"), "policy review must pass"),
        _check("policy_review_ready", summary.get("bounded_objective_decoder_anchor_policy_review_ready") is True, summary.get("bounded_objective_decoder_anchor_policy_review_ready"), "policy review must be ready"),
        _check("assisted_anchor_path_closed", summary.get("assisted_anchor_path_closed") is True, summary.get("assisted_anchor_path_closed"), "unassisted repair plan requires the assisted anchor path to be closed"),
        _check("selected_track_unassisted_repair", summary.get("selected_track") == "unassisted_objective_repair", summary.get("selected_track"), "review must select unassisted objective repair"),
        _check("new_text_still_failed", int(signals.get("new_text_pass_count") or 0) == 0, signals.get("new_text_pass_count"), "plan should run only when new-text replay is still failing"),
    ]


def _plan(status: str, work_items: list[dict[str, Any]], gates: list[dict[str, Any]], blocked_actions: list[dict[str, Any]]) -> dict[str, Any]:
    return {
        "ready": status == "pass",
        "route": "bounded_objective_unassisted_repair",
        "work_item_count": len(work_items),
        "acceptance_gate_count": len(gates),
        "blocked_action_count": len(blocked_actions),
        "promotion_ready": False,
        "new_training_allowed": False,
        "proposed_next_artifact": "model_capability_route_promotion_bounded_objective_unassisted_repair_seed",
        "next_step": "build_bounded_objective_unassisted_repair_seed" if status == "pass" else "repair_unassisted_repair_plan_inputs",
    }


def _summary(status: str, checks: list[dict[str, Any]], plan: dict[str, Any]) -> dict[str, Any]:
    return {
        "bounded_objective_unassisted_repair_plan_ready": status == "pass" and plan.get("ready") is True,
        "route": plan.get("route"),
        "work_item_count": plan.get("work_item_count"),
        "acceptance_gate_count": plan.get("acceptance_gate_count"),
        "blocked_action_count": plan.get("blocked_action_count"),
        "promotion_ready": plan.get("promotion_ready"),
        "new_training_allowed": plan.get("new_training_allowed"),
        "proposed_next_artifact": plan.get("proposed_next_artifact"),
        "next_step": plan.get("next_step"),
        "failed_check_count": sum(1 for row in checks if row["status"] != "pass"),
    }


def _decision(status: str) -> str:
    if status == "pass":
        return "model_capability_route_promotion_bounded_objective_unassisted_repair_plan_ready"
    return "fix_model_capability_route_promotion_bounded_objective_unassisted_repair_plan"


def _interpretation(status: str, plan: dict[str, Any]) -> dict[str, Any]:
    if status != "pass":
        return {"model_quality_claim": "not_claimed", "reason": "Unassisted repair plan inputs are incomplete.", "next_action": "repair unassisted repair plan inputs"}
    return {
        "model_quality_claim": "plan_only",
        "reason": "The assisted anchor path is closed; the next work must target unassisted required-term generation and holdout replay.",
        "next_action": plan.get("next_step"),
    }


